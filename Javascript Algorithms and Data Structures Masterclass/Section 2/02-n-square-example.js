
function countUpAndDown( n ){
    console.log('Para arriba!');
    
    for (let i = 0; i < n; i++) {
        console.log(i);
    }
    
    console.log('Hasta arriba!\n Yendo para abajo!');

    for (let j = n-1; j >= 0; j--) {
        console.log(j);
    }

    console.log('De nuevo abajo. Adios!');

}




